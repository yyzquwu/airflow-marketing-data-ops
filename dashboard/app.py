def normalize_global_ads_performance(raw: pd.DataFrame) -> pd.DataFrame:
    source_map = {
        "Google Ads": "google_ads",
        "Meta Ads": "meta_ads",
        "Meta": "meta_ads",
        "TikTok Ads": "tiktok_ads",
        "TikTok": "tiktok_ads",
    }
    medium_map = {
        "Display": ("display", "Display"),
        "Search": ("paid_search", "Paid Search"),
        "Shopping": ("paid_shopping", "Paid Shopping"),
        "Video": ("paid_video", "Paid Video"),
    }
    rows = raw.copy().reset_index(drop=True)
    rows["source_label"] = rows["platform"].map(normalize_source_label)
    rows["source"] = rows["platform"].map(source_map).fillna(rows["source_label"].str.lower().str.replace(" ", "_"))
    medium_pairs = rows["campaign_type"].map(medium_map)
    rows["medium"] = medium_pairs.map(lambda pair: pair[0] if isinstance(pair, tuple) else "uploaded")
    rows["medium_label"] = medium_pairs.map(lambda pair: pair[1] if isinstance(pair, tuple) else "Uploaded")
    rows["source_medium"] = rows["medium_label"]
    rows["campaign_id"] = [f"GA-{index + 1:05d}" for index in rows.index]
    rows["campaign_name"] = rows["industry"].astype(str) + " " + rows["campaign_type"].astype(str) + " " + rows["country"].astype(str)
    rows["segment"] = rows["country"]
    rows["spend"] = rows["ad_spend"]
    rows["total_conversion"] = rows["conversions"]
    rows["ctr"] = rows["CTR"]
    rows["cpc"] = rows["CPC"]
    rows["cpa"] = rows["CPA"]
    rows["roas"] = rows["ROAS"]
    rows["dataset"] = "global_ads_performance"
    rows["account_id"] = "kaggle-global-ads"
    return prepare_campaign_data(rows)


