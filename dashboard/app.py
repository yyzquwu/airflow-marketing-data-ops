def render_trend_chart(df: pd.DataFrame, grain: str) -> None:
    trend = time_series(df, grain)
    spend_hover = [
        (
            f"<b>{escape(row.label)}</b><br>"
            f"Spend: {money(float(row.spend))}<br>"
            f"Revenue: {money(float(row.revenue))}<br>"
            f"ROAS: {float(row.roas):.2f}x<br>"
            f"CPA: {money(float(row.cpa))}"
        )
        for row in trend.itertuples(index=False)
    ]
    conversion_hover = [
        (
            f"<b>{escape(row.label)}</b><br>"
            f"Conversions: {number(float(row.conversions))}<br>"
            f"Clicks: {number(float(row.clicks))}<br>"
            f"CTR: {percent(float(row.ctr))}<br>"
            f"CPC: {money(float(row.cpc))}"
        )
        for row in trend.itertuples(index=False)
    ]
    fig = make_subplots(specs=[[{"secondary_y": True}]])
    fig.add_trace(
        go.Scatter(
            x=trend["period"],
            y=trend["spend"],
            name="Spend",
            mode="lines+markers",
            line=dict(color="#2563eb", width=3),
            marker=dict(size=8),
            hovertext=spend_hover,
            hovertemplate="%{hovertext}<extra></extra>",
        ),
        secondary_y=False,
    )
    fig.add_trace(
        go.Bar(
            x=trend["period"],
            y=trend["conversions"],
            name="Conversions",
            marker_color="#0f766e",
            opacity=0.78,
            hovertext=conversion_hover,
            hovertemplate="%{hovertext}<extra></extra>",
        ),
        secondary_y=True,
    )
    fig.update_layout(title="Spend & Conversions Over Time")
    if grain == "Month":
        fig.update_xaxes(tickformat="%b %Y")
    elif grain == "Week":
        fig.update_xaxes(tickformat="%b %d", dtick=7 * 24 * 60 * 60 * 1000)
    else:
        tick_values = trend["period"].iloc[::5].tolist()
        if trend["period"].iloc[-1] not in tick_values:
            tick_values.append(trend["period"].iloc[-1])
        fig.update_xaxes(
            tickmode="array",
            tickvals=tick_values,
            ticktext=[value.strftime("%b %d") for value in tick_values],
        )
    fig.update_yaxes(title_text="Spend", secondary_y=False)
    fig.update_yaxes(title_text="Conversions", secondary_y=True)
    st.plotly_chart(style_figure(fig, 390), width="stretch")


