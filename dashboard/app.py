def time_series(df: pd.DataFrame, grain: str) -> pd.DataFrame:
    daily = df.copy()
    if grain == "Week":
        daily["period"] = daily["date"].dt.to_period("W").dt.start_time
        label_format = "%b %d"
    elif grain == "Month":
        daily["period"] = daily["date"].dt.to_period("M").dt.start_time
        label_format = "%b %Y"
    else:
        daily["period"] = daily["date"]
        label_format = "%b %d"

    trend = (
        daily.groupby("period", as_index=False)[METRIC_COLUMNS]
        .sum()
        .sort_values("period")
    )
    trend["ctr"] = trend.apply(lambda row: safe_divide(row["clicks"], row["impressions"]), axis=1)
    trend["cpc"] = trend.apply(lambda row: safe_divide(row["spend"], row["clicks"]), axis=1)
    trend["cpa"] = trend.apply(lambda row: safe_divide(row["spend"], row["conversions"]), axis=1)
    trend["roas"] = trend.apply(lambda row: safe_divide(row["revenue"], row["spend"]), axis=1)
    trend["label"] = trend["period"].dt.strftime(label_format)
    return trend


def available_trend_grains(df: pd.DataFrame) -> list[str]:
    options = ["Day"]
    for grain in ["Week", "Month"]:
        if len(time_series(df, grain)) > 1:
            options.append(grain)
    return options


def reset_invalid_widget_value(key: str, allowed_values: list[str], default_value: str) -> None:
    if st.session_state.get(key) not in allowed_values:
        st.session_state[key] = default_value


def reset_invalid_date_range(key: str, min_date: object, max_date: object) -> None:
    value = st.session_state.get(key)
    if not isinstance(value, tuple) or len(value) != 2:
        st.session_state[key] = (min_date, max_date)
        return

    start, end = value
    if start < min_date or end > max_date or start > end:
        st.session_state[key] = (min_date, max_date)


