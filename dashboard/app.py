def safe_divide(numerator: float, denominator: float) -> float:
    return 0.0 if denominator == 0 else numerator / denominator


def normalize_source_label(value: object) -> str:
    text = "" if pd.isna(value) else str(value)
    return SOURCE_LABELS.get(text, text or "Uploaded")


def normalize_medium_label(row: pd.Series) -> str:
    explicit = row.get("source_medium") or row.get("medium_label")
    if explicit and not pd.isna(explicit):
        return str(explicit)
    source_label = normalize_source_label(row.get("source"))
    return SOURCE_MEDIUM.get(source_label, "Paid Media")


def infer_industry(campaign_name: object) -> str:
    text = "" if pd.isna(campaign_name) else str(campaign_name)
    for marker in [" Search ", " Display ", " Shopping ", " Video "]:
        if marker in text:
            return text.split(marker, 1)[0]
    return "Unknown"


def prepare_campaign_data(df: pd.DataFrame) -> pd.DataFrame:
    missing_columns = sorted(set(REQUIRED_COLUMNS) - set(df.columns))
    if missing_columns:
        joined = ", ".join(missing_columns)
        raise ValueError(f"Uploaded CSV is missing required columns: {joined}")

    prepared = df.copy()
    if "account_id" not in prepared.columns:
        prepared["account_id"] = ""
    if "segment" not in prepared.columns:
        prepared["segment"] = ""
    if "total_conversion" not in prepared.columns:
        prepared["total_conversion"] = prepared["conversions"]
    if "revenue" not in prepared.columns:
        prepared["revenue"] = 0
    if "roas" not in prepared.columns:
        prepared["roas"] = 0
    if "dataset" not in prepared.columns:
        prepared["dataset"] = "uploaded"

    prepared["date"] = pd.to_datetime(prepared["date"], errors="coerce", format="mixed").dt.normalize()
    if prepared["date"].isna().any():
        raise ValueError("CSV contains rows with invalid dates.")

    for column in METRIC_COLUMNS + ["ctr", "cpc", "cpa", "total_conversion", "roas"]:
        prepared[column] = pd.to_numeric(prepared[column], errors="coerce").fillna(0)

    prepared["ctr"] = prepared.apply(lambda row: row["ctr"] or safe_divide(row["clicks"], row["impressions"]), axis=1)
    prepared["cpc"] = prepared.apply(lambda row: row["cpc"] or safe_divide(row["spend"], row["clicks"]), axis=1)
    prepared["cpa"] = prepared.apply(lambda row: row["cpa"] or safe_divide(row["spend"], row["conversions"]), axis=1)
    prepared["roas"] = prepared.apply(lambda row: row["roas"] or safe_divide(row["revenue"], row["spend"]), axis=1)
    if "source_label" not in prepared.columns:
        prepared["source_label"] = prepared["source"].map(normalize_source_label)
    else:
        prepared["source_label"] = prepared["source_label"].map(normalize_source_label)
    prepared["source_medium"] = prepared.apply(normalize_medium_label, axis=1)
    if "country" not in prepared.columns:
        prepared["country"] = prepared["segment"].replace("", "Unknown")
    if "industry" not in prepared.columns:
        prepared["industry"] = prepared["campaign_name"].map(infer_industry)
    return prepared.sort_values(["date", "source_label", "campaign_name"]).reset_index(drop=True)


