def render_efficiency_chart(df: pd.DataFrame) -> None:
    campaigns = campaign_summary(df).query("conversions > 0")
    conversion_threshold = max(1.0, float(campaigns["conversions"].sum()) * 0.0025)
    if len(campaigns) > 20:
        campaigns = campaigns[campaigns["conversions"] >= conversion_threshold]
    summary = campaigns.sort_values("cpa", ascending=True).head(9)
    summary["hover_text"] = summary.apply(
        lambda row: (
            f"<b>{escape(str(row['campaign_name']))}</b><br>"
            f"Platform: {escape(str(row['source_label']))}<br>"
            f"CPA: {money(float(row['cpa']))}<br>"
            f"Spend: {money(float(row['spend']))}<br>"
            f"Conversions: {number(float(row['conversions']))}<br>"
            f"Revenue: {money(float(row['revenue']))}<br>"
            f"CTR: {percent(float(row['ctr']))}<br>"
            f"ROAS: {float(row['roas']):.2f}x"
        ),
        axis=1,
    )
    fig = px.bar(
        summary,
        x="cpa",
        y="campaign_name",
        color="source_label",
        color_discrete_map=PLATFORM_COLORS,
        orientation="h",
        title=f"CPA by Campaign (min. {number(conversion_threshold)} conversions)",
        labels={"campaign_name": "", "cpa": "CPA", "source_label": "Platform"},
    )
    hover_lookup = {
        (str(row.campaign_name), str(row.source_label)): str(row.hover_text)
        for row in summary.itertuples(index=False)
    }
    for trace in fig.data:
        trace.hovertext = [hover_lookup.get((str(campaign), str(trace.name)), "") for campaign in trace.y]
        trace.hovertemplate = "%{hovertext}<extra></extra>"
    fig.update_layout(yaxis=dict(autorange="reversed"))
    st.plotly_chart(style_figure(fig, 390), width="stretch")


